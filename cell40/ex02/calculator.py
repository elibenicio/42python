#!/usr/bin/env python3
first_number=int(input("Give me the first number:"))
second_number=int(input("Give me the second number:"))
print("Thank you!")
intRes=first_number+second_number
print(str(first_number),"+",str(second_number),"=",str(intRes))
intRes=first_number-second_number
print(str(first_number),"-",str(second_number),"=",str(intRes))
intRes=first_number/second_number
print(str(first_number),"/",str(second_number),"=",str(intRes))
intRes=first_number*second_number
print(str(first_number),"*",str(second_number),"=",str(intRes))
